public class PiecewiseFunction3 {

    public static void main(String[] args) {

        double x = 0;
        double y;

        if (x == 0) {
            y = 0;
        } else {
            y = 1 / x;
        }

        System.out.println("x = " + x);
        System.out.println("y = " + y);
    }
}